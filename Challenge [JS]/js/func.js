let base = parseInt(prompt('Ingrese la medida base de su triangulo: '))
let altura = parseInt(prompt('Ingrese la medida de la altura de su triangulo: '))
let areaT = (base*altura)/2

alert('El área de su triangulo está mostrada en pantalla.')

document.write('<br/>El área del triangulo es: ' + areaT)

const pi = 3.14
let radio = parseInt(prompt('Ingrese el radio de su circulo: '))
let areaC = ((pi)*radio**2)

alert('El área de su circulo está en pantalla.')

document.write('<br/> El área del circulo es : ' + areaC)